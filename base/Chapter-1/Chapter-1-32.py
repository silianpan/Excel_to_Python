#对齐设置
print('|{:<10}|'.format('曾贤志'))     #左对齐，不足部分用空格填充。
print('|{:□<10}|'.format('曾贤志'))    #左对齐，不足部分用□填充。
print('|{:>10}|'.format('曾贤志'))     #右对齐，不足部分用空格填充。
print('|{:□>10}|'.format('曾贤志'))    #右对齐，不足部分用□填充。
print('|{:^10}|'.format('曾贤志'))     #居中对齐，不足部分用空格填充。
print('|{:□^10}|'.format('曾贤志'))    #居中对齐，不足部分用□填充。


