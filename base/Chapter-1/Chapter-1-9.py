num='99'

print(num)#返回'99'
print(type(num))#返回<class 'str'>

print(int(num))#返回99
print(type(int(num)))#返回<class 'int'>

print(float(num))#返回99.0
print(type(float(num)))#返回<class 'float'>

print(complex(num))#返回(99+0j)
print(type(complex(num)))#返回<class 'complex'>
